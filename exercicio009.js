//Crie um programa de contagem progressiva. 
//Peça um número e conte de 0 até esse número,
//usando um loop while no console do navegador.

let contador = 0;
numeroEscolhido = parseInt(prompt('Digite um número positivo(Maior que zero)'))
while (contador <= numeroEscolhido) {
    alert(contador);
    contador++;
}
